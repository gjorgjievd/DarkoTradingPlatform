function New-DeployRestoreDbScript {
    return @'
#Requires -Version 5.1
$ErrorActionPreference = 'Stop'
Set-Location $PSScriptRoot

$backupDir = '.\backups'
if (-not (Test-Path -LiteralPath $backupDir)) {
    throw 'No backups folder found.'
}

$backups = Get-ChildItem -LiteralPath $backupDir -Filter 'tradingagent_*.db' | Sort-Object LastWriteTime -Descending
if ($backups.Count -eq 0) {
    throw 'No backup files found.'
}

Write-Host 'Available backups:' -ForegroundColor Cyan
for ($i = 0; $i -lt $backups.Count; $i++) {
    Write-Host ("[{0}] {1} ({2})" -f ($i + 1), $backups[$i].Name, $backups[$i].LastWriteTime)
}

$selection = Read-Host 'Select backup number'
$parsed = 0
if (-not [int]::TryParse($selection, [ref]$parsed)) {
    throw 'Invalid selection.'
}
$index = $parsed - 1
if ($index -lt 0 -or $index -ge $backups.Count) {
    throw 'Selection out of range.'
}

$confirm = Read-Host "Restore $($backups[$index].Name)? This will overwrite the current database. [Y/N]"
if ($confirm -notmatch '^[Yy]') {
    Write-Host 'Restore cancelled.'
    exit 0
}

if (-not (Test-Path -LiteralPath '.\data')) {
    New-Item -ItemType Directory -Path '.\data' | Out-Null
}

docker compose --env-file .env down | Out-Null
Copy-Item -LiteralPath $backups[$index].FullName -Destination '.\data\tradingagent.db' -Force
docker compose --env-file .env up -d
if ($LASTEXITCODE -ne 0) { throw 'Failed to restart containers after restore.' }
Write-Host 'Database restored and containers restarted.' -ForegroundColor Green
'@
}

