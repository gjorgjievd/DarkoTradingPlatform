function New-DeployHealthcheckScript {
    return @'
#Requires -Version 5.1
$ErrorActionPreference = 'Continue'
Set-Location $PSScriptRoot

$results = [ordered]@{}

function Test-Check {
    param([string]$Name, [scriptblock]$Action)
    try {
        & $Action
        $script:results[$Name] = 'PASS'
        Write-Host "[PASS] $Name" -ForegroundColor Green
    } catch {
        $script:results[$Name] = "FAIL: $($_.Exception.Message)"
        Write-Host "[FAIL] $Name - $($_.Exception.Message)" -ForegroundColor Red
    }
}

Test-Check 'Container running' {
    $running = @(docker ps --filter 'name=tradingagent' --format '{{.Names}}' 2>$null)
    if ($running.Count -eq 0 -or [string]::IsNullOrWhiteSpace($running[0])) {
        throw 'No running TradingAgent containers found.'
    }
}

Test-Check 'Database reachable' {
    $db = '.\data\tradingagent.db'
    if (-not (Test-Path -LiteralPath $db)) {
        throw 'Database file does not exist yet.'
    }
}

Test-Check 'Dashboard endpoint' {
    $response = Invoke-WebRequest -Uri 'http://localhost:8085/' -UseBasicParsing -TimeoutSec 10
    if ($response.StatusCode -ne 200) { throw "HTTP $($response.StatusCode)" }
}

Test-Check 'Market status API' {
    $response = Invoke-RestMethod -Uri 'http://localhost:8085/api/market/status' -TimeoutSec 10
    if (-not $response.marketSession) { throw 'Invalid market status response.' }
}

Test-Check 'Webhook endpoint' {
    $body = '{"symbol":"HEALTH","signal":"BUY","secret":"invalid"}'
    try {
        Invoke-WebRequest -Uri 'http://localhost:8085/api/tradingview/webhook' -Method POST -ContentType 'application/json' -Body $body -UseBasicParsing -TimeoutSec 10 | Out-Null
        throw 'Expected unauthorized response.'
    } catch {
        if ($_.Exception.Response.StatusCode.value__ -ne 401) {
            throw 'Webhook endpoint did not return expected 401 for invalid secret.'
        }
    }
}

Test-Check 'Claude configuration' {
    $settings = Invoke-RestMethod -Uri 'http://localhost:8085/api/settings' -TimeoutSec 10
    if ($settings.CLAUDE_ENABLED -and -not $settings.CLAUDE_MODEL) {
        throw 'Claude enabled but model not configured.'
    }
}

Test-Check 'Telegram configuration' {
    $settings = Invoke-RestMethod -Uri 'http://localhost:8085/api/settings' -TimeoutSec 10
    if ($settings -is [pscustomobject]) {
        Write-Host '  Telegram settings present (token/chat configured via .env).' -ForegroundColor DarkGray
    }
}

Test-Check 'Yahoo Finance reachable' {
    $yahoo = Invoke-RestMethod -Uri 'http://localhost:8085/api/test-yahoo' -TimeoutSec 30
    if (-not $yahoo.symbol) { throw 'Yahoo test response invalid.' }
}

Write-Host ''
$failed = @($results.GetEnumerator() | Where-Object { $_.Value -like 'FAIL*' })
if ($failed.Count -eq 0) {
    Write-Host 'HEALTH CHECK: PASS' -ForegroundColor Green
    exit 0
}

Write-Host 'HEALTH CHECK: FAIL' -ForegroundColor Red
foreach ($item in $failed) {
    Write-Host " - $($item.Key): $($item.Value)" -ForegroundColor Red
}
exit 1
'@
}

function Write-DeploymentPackageFiles {
    param(
        [string] $DeployDir,
        [string] $DevEnvPath,
        [string] $NginxSource,
        [string] $BuildTimestamp
    )

    if (-not (Test-Path -LiteralPath $DeployDir)) {
        New-Item -ItemType Directory -Path $DeployDir | Out-Null
    }

    $utf8NoBom = New-Object System.Text.UTF8Encoding $false
    $files = [ordered]@{
        'docker-compose.yml' = New-DeploymentDockerCompose
        '.env' = New-DeploymentEnvContent -DevEnvPath $DevEnvPath
        '.env.example' = New-DeploymentEnvContent -DevEnvPath $DevEnvPath -ExampleOnly
        'README.md' = New-DeploymentReadme -BuildTimestamp $BuildTimestamp
        'start.ps1' = New-DeployStartScript
        'stop.ps1' = New-DeployStopScript
        'restart.ps1' = New-DeployRestartScript
        'status.ps1' = New-DeployStatusScript
        'logs.ps1' = New-DeployLogsScript
        'backup-db.ps1' = New-DeployBackupDbScript
        'restore-db.ps1' = New-DeployRestoreDbScript
        'update-image.ps1' = New-DeployUpdateImageScript
        'clean.ps1' = New-DeployCleanScript
        'healthcheck.ps1' = New-DeployHealthcheckScript
    }

    foreach ($entry in $files.GetEnumerator()) {
        $target = Join-Path $DeployDir $entry.Key
        [System.IO.File]::WriteAllText($target, $entry.Value, $utf8NoBom)
    }

    Copy-Item -LiteralPath $NginxSource -Destination (Join-Path $DeployDir 'nginx.conf') -Force
}

function Test-DeploymentPackage {
    param(
        [string] $DeployDir,
        [string] $ImageName
    )

    $required = @(
        'docker-compose.yml',
        '.env',
        '.env.example',
        'README.md',
        'nginx.conf',
        'tradingagent-image.tar',
        'start.ps1',
        'stop.ps1',
        'restart.ps1',
        'status.ps1',
        'logs.ps1',
        'clean.ps1',
        'backup-db.ps1',
        'restore-db.ps1',
        'update-image.ps1',
        'healthcheck.ps1'
    )

    $missing = @()
    foreach ($file in $required) {
        if (-not (Test-Path -LiteralPath (Join-Path $DeployDir $file))) {
            $missing += $file
        }
    }

    if ($missing.Count -gt 0) {
        throw "Deployment package is missing files: $($missing -join ', ')"
    }

    docker image inspect $ImageName | Out-Null
    if ($LASTEXITCODE -ne 0) {
        throw "Docker image '$ImageName' was not found after build."
    }

    $scriptErrors = @()
    foreach ($scriptName in $required | Where-Object { $_ -like '*.ps1' }) {
        $tokens = $null
        $errors = $null
        $null = [System.Management.Automation.Language.Parser]::ParseFile(
            (Join-Path $DeployDir $scriptName),
            [ref]$tokens,
            [ref]$errors)
        if ($errors.Count -gt 0) {
            $scriptErrors += "$scriptName : $($errors[0].Message)"
        }
    }

    if ($scriptErrors.Count -gt 0) {
        throw "Script validation failed:`n$($scriptErrors -join "`n")"
    }
}
