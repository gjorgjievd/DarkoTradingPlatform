``````powershell
.\backup-db.ps1
.\restore-db.ps1
``````

Backups are stored in ``backups\``.

## Logs

``````powershell
.\logs.ps1
``````

## Health check

``````powershell
.\healthcheck.ps1
``````

## TradingView setup

1. Create an alert in TradingView
2. Set webhook URL to: ``http://<your-server-ip>:8085/api/tradingview/webhook``
3. Use JSON body with ``symbol``, ``signal``, and ``secret`` matching ``WEBHOOK_SECRET`` in ``.env``
4. Use **string** values for ``price`` and ``rsi`` (not raw numbers)

Example payload:

``````json
{
  "symbol": "NVDA",
  "signal": "BUY",
  "price": "199.20",
  "timeframe": "1H",
  "strategy": "EMA_RSI",
  "secret": "your-webhook-secret"
}
``````

## Telegram setup

1. Create a bot via [@BotFather](https://t.me/BotFather)
2. Start a chat with the bot and send a message
3. Open ``https://api.telegram.org/bot<TOKEN>/getUpdates`` to find ``chat.id``
4. Set ``TELEGRAM_BOT_TOKEN`` and ``TELEGRAM_CHAT_ID`` in ``.env``
5. Run ``.\restart.ps1``

## Claude setup

1. Create an Anthropic API key
2. Set ``CLAUDE_API_KEY`` in ``.env``
3. Set ``CLAUDE_ENABLED=true``
4. Run ``.\restart.ps1``

## Yahoo Finance

Market data is fetched automatically at runtime. Test with:

``````powershell
Invoke-RestMethod http://localhost:8085/api/test-yahoo
``````

## Troubleshooting

| Issue | Fix |
|-------|-----|
| Docker not running | Start Docker Desktop and wait until it is ready |
| Port 8085 in use | Change ``8085:80`` in ``docker-compose.yml`` |
| Webhook 400 Invalid JSON | Send ``price``/``rsi`` as strings in JSON body |
| Webhook 401 | Check ``WEBHOOK_SECRET`` matches payload/header |
| Market closed signals ignored | Expected on weekends/holidays; see ``ENABLE_24_5_TRADING`` |
| Database missing | Run ``.\start.ps1`` — creates ``data\`` automatically |

