function New-DeploymentEnvContent {
    param(
        [string] $DevEnvPath,
        [switch] $ExampleOnly
    )

    $defaults = Get-DefaultEnvValues
    $devValues = Read-DotEnvFile -Path $DevEnvPath
    $secretKeys = @('TELEGRAM_BOT_TOKEN', 'TELEGRAM_CHAT_ID', 'CLAUDE_API_KEY', 'ANTHROPIC_API_KEY')

    $lines = New-Object System.Collections.Generic.List[string]
    $lines.Add('# TradingAgent environment configuration')
    $lines.Add('# Generated by build-deployment-package.ps1')
    $lines.Add('')

    foreach ($entry in $defaults.GetEnumerator()) {
        $key = [string]$entry.Key
        $value = [string]$entry.Value

        if ($devValues.Contains($key) -and $devValues[$key]) {
            $value = [string]$devValues[$key]
        }

        if ($ExampleOnly -or ($secretKeys -contains $key)) {
            $value = ''
        }

        $lines.Add("$key=$value")
    }

    return ($lines -join [Environment]::NewLine) + [Environment]::NewLine
}

