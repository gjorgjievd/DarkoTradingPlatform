function New-DeployStartScript {
    return @'
#Requires -Version 5.1
$ErrorActionPreference = 'Stop'
Set-Location $PSScriptRoot

function Write-Info([string]$Message) { Write-Host $Message -ForegroundColor Cyan }
function Write-Ok([string]$Message) { Write-Host $Message -ForegroundColor Green }
function Write-Warn([string]$Message) { Write-Host $Message -ForegroundColor Yellow }

Write-Info 'Checking Docker Desktop...'
if (-not (Get-Command docker -ErrorAction SilentlyContinue)) {
    throw 'Docker is not installed. Install Docker Desktop for Windows first.'
}

docker info *> $null
if ($LASTEXITCODE -ne 0) {
    throw 'Docker engine is not running. Start Docker Desktop and try again.'
}
Write-Ok 'Docker engine is running.'

$imageName = 'tradingagent:latest'
$imageExists = docker image inspect $imageName *> $null
if ($LASTEXITCODE -ne 0) {
    Write-Info 'Docker image not found locally. Loading tradingagent-image.tar...'
    if (-not (Test-Path -LiteralPath '.\tradingagent-image.tar')) {
        throw 'tradingagent-image.tar not found in deployment folder.'
    }
    docker load -i .\tradingagent-image.tar
    if ($LASTEXITCODE -ne 0) { throw 'docker load failed.' }
    Write-Ok 'Docker image loaded.'
} else {
    Write-Ok 'Docker image already present.'
}

foreach ($folder in @('data', 'logs', 'backups')) {
    if (-not (Test-Path -LiteralPath $folder)) {
        New-Item -ItemType Directory -Path $folder | Out-Null
        Write-Ok "Created folder: $folder"
    }
}

if (-not (Test-Path -LiteralPath '.\.env')) {
    if (Test-Path -LiteralPath '.\.env.example') {
        Copy-Item -LiteralPath '.\.env.example' -Destination '.\.env'
        Write-Warn '.env was missing — copied from .env.example. Edit API keys before production use.'
    } else {
        throw '.env file is missing.'
    }
}

Write-Info 'Starting containers...'
docker compose --env-file .env up -d
if ($LASTEXITCODE -ne 0) { throw 'docker compose up failed.' }

Write-Info 'Waiting for application to become healthy...'
$healthy = $false
for ($attempt = 1; $attempt -le 45; $attempt++) {
    try {
        $response = Invoke-WebRequest -Uri 'http://localhost:8085/api/market/status' -UseBasicParsing -TimeoutSec 5
        if ($response.StatusCode -eq 200) {
            $healthy = $true
            break
        }
    } catch {
        Start-Sleep -Seconds 2
    }
}

if (-not $healthy) {
    Write-Warn 'Application did not respond in time. Check logs with .\logs.ps1'
} else {
    Write-Ok 'Application is healthy.'
}

$imageId = (docker image inspect tradingagent:latest --format '{{.Id}}' 2>$null)
$shortId = if ($imageId) { $imageId.Substring(7, [Math]::Min(12, $imageId.Length - 7)) } else { 'unknown' }

Write-Host ''
Write-Ok 'TradingAgent started successfully.'
Write-Host 'Dashboard URL : http://localhost:8085/'
Write-Host 'API reference : http://localhost:8085/api/settings'
Write-Host 'Webhook URL   : http://localhost:8085/api/tradingview/webhook'
Write-Host "Image version : $shortId"
Write-Host 'Database path : $((Resolve-Path .\data).Path)\tradingagent.db'
Write-Host ''
docker compose ps
'@
}

